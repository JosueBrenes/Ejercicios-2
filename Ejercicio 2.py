print("Digite el numero A: ")
primerNumero = input()
primerNumero = int(primerNumero)

print("Digite el numero B: ")
segundoNumero = input()
segundoNumero = int(segundoNumero)

# = Se suman las variables e imprimimos el resultado
suma = (primerNumero + segundoNumero)
print("El resultado de la suma es: " + str(suma))

# = Se restan las variables e imprimimos el resultado
resta = (primerNumero + segundoNumero)
print("El resultado de la resta es: " + str(resta))

# = Se dividen las variables e imprimimos el resultado
division = (primerNumero / segundoNumero)
print("El resultado de la resta es: " + str(division))

# = Se multiplican las variables e imprimimos el resultado
multiplicacion = (primerNumero * segundoNumero)
print("El resultado de la multiplicacion es de: " + str(multiplicacion))